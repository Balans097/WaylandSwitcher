## core.nim — разделяемое состояние между потоками и подсистема логирования.
##
## ── Разделяемое состояние ────────────────────────────────────────────────────
##
##   Обычные bool-переменные, разделяемые между главным потоком и потоком
##   мыши, на ARM и других слабо упорядоченных архитектурах дают
##   неопределённое поведение из-за отсутствия барьеров памяти.
##
##   Все флаги, читаемые/записываемые из нескольких потоков, объявлены как
##   Atomic[bool] из модуля std/atomics. Операции используют явный барьер
##   (по умолчанию moSeqCst), что гарантирует корректную видимость без
##   полноценного мьютекса (гонки за структурами данных нет: буфер keyBuf
##   принадлежит исключительно главному потоку).
##
##   mouseFDGlobal инициализируется до запуска потока мыши и не меняется
##   после — обычный cint безопасен (однократная запись до старта потока
##   гарантирует видимость на x86/ARM).
##
## ── Логирование ──────────────────────────────────────────────────────────────
##
##   Правила маршрутизации logMsg:
##     • daemon-режим  → только syslog  (stdOnly=false)
##     • daemon-режим  → только stdout  (stdOnly=true, отладка без syslog)
##     • debug-режим   → только stdout  (всегда, syslog не используется)
##
##   Вызов logMsg из обработчика сигнала ЗАПРЕЩЁН: async-signal-unsafe.
##   Вместо этого обработчик только пишет в stopAndExit, а главный цикл
##   логирует факт завершения самостоятельно.

import std/atomics
import types

# ── Разделяемое состояние ─────────────────────────────────────────────────────

var
  ## Главный поток устанавливает true после добавления символов в буфер;
  ## поток мыши читает, чтобы понять, стоит ли реагировать на клик мышью.
  needTrackMouse*: Atomic[bool]

  ## Поток мыши устанавливает true при обнаружении клика;
  ## главный поток читает флаг и сбрасывает буфер нажатий.
  needClearKeyBuf*: Atomic[bool]

  ## Устанавливается обработчиком сигнала (SIGHUP/SIGINT/SIGQUIT/SIGTERM)
  ## через атомарную запись; главный цикл проверяет и завершается корректно.
  stopAndExit*: Atomic[bool]

  ## Файловый дескриптор мыши.
  ## Записывается один раз в главном потоке до старта mouseThreadFn,
  ## после чего используется только для чтения — дополнительная синхронизация
  ## не требуется.
  mouseFDGlobal*: cint = -1

proc initSharedState*() =
  ## Сбрасывает все атомарные флаги и дескриптор мыши в начальное состояние.
  ## Вызывать перед запуском любых фоновых потоков.
  needTrackMouse.store(false)
  needClearKeyBuf.store(false)
  stopAndExit.store(false)
  mouseFDGlobal = -1

# ── Логирование ───────────────────────────────────────────────────────────────

var
  ## true  → приложение работает как демон: stdout закрыт, вывод идёт в syslog.
  ## false → интерактивный/debug режим: вывод только в stdout.
  daemonMode*: bool = true

# Привязки к функциям syslog из libc
proc c_openlog*(id: cstring; opt, fac: cint) {.importc: "openlog",  header: "<syslog.h>".}
proc c_syslog*(pri: cint; fmt: cstring)       {.importc: "syslog",   header: "<syslog.h>", varargs.}
proc c_closelog*()                             {.importc: "closelog", header: "<syslog.h>".}

proc logMsg*(isErr: bool; msg: string; stdOnly = false) =
  ## Записывает сообщение согласно текущему режиму работы.
  ##
  ## isErr   — true → приоритет LOG_ERR, false → LOG_INFO.
  ## stdOnly — true → принудительно в stdout, не в syslog
  ##           (используется для трассировочного вывода в debug-режиме,
  ##            который не нужен в системном журнале).
  let pri = if isErr: LOG_ERR else: LOG_INFO
  if daemonMode:
    if not stdOnly:
      c_syslog(pri, "%s", cstring(msg))
    # stdOnly=true в daemon-режиме → тихо игнорируем (нет терминала)
  else:
    # Интерактивный режим: только stdout, syslog не трогаем вовсе —
    # пользователь видит вывод в терминале, дублирование излишне.
    echo msg
